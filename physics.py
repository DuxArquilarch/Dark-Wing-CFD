def air_properties(altitude_m=0.0):
    T   = 288.15 - 0.0065 * altitude_m
    P   = 101325.0 * (T / 288.15) ** 5.2561
    rho = P / (287.05 * T)
    mu  = 1.458e-6 * T**1.5 / (T + 110.4)
    return rho, mu

